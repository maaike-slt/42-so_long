#include "libft.h"

static bool	ft_isin(const char *s, char c)
{
	size_t	i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			return (true);
		i++;
	}
	return (false);
}

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	start;
	size_t	end;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	while (s1[start] != '\0' && ft_isin(set, s1[start]))
		start++;
	end = ft_strlen(s1) - 1;
	while (end + 1 > 0 && ft_isin(set, s1[end]))
		end--;
	if (start > end)
		return (ft_strdup("\0"));
	return (ft_substr(s1, start, end - start + 1));
}
