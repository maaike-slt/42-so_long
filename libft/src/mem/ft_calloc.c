#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*dst;
	size_t	total;

	if ((double)nmemb * (double)size >= (double)__SIZE_MAX__)
		return (NULL);
	total = nmemb * size;
	dst = malloc(total);
	if (!dst)
		return (NULL);
	ft_bzero(dst, total);
	return (dst);
}
